const MAX_PER_QUERY = 18;
const MAX_PER_CATEGORY = 160;

function scoreVideo(meta, query, category) {
  const text = `${meta.title || ''} ${meta.channel || ''}`.toLowerCase();
  let score = 0;
  if (text.includes('입문')) score += 3;
  if (text.includes('기초')) score += 3;
  if (text.includes('초보')) score += 2;
  if (text.includes('한국어')) score += 1;
  if (text.includes('tutorial')) score += 1;
  if (text.includes('활용')) score += 1;
  if (text.includes('광고')) score -= 5;
  if (text.includes('홍보')) score -= 3;
  if (text.includes('shorts')) score -= 1;
  if ((meta.title || '').length > 8) score += 1;
  if (category === 'senior' && (text.includes('시니어') || text.includes('50대') || text.includes('60대') || text.includes('중장년'))) score += 4;
  if (category === 'study' && (text.includes('공부') || text.includes('학습') || text.includes('과제') || text.includes('학생'))) score += 3;
  if (category === 'work' && (text.includes('업무') || text.includes('직장인') || text.includes('생산성'))) score += 3;
  if (category === 'hobby' && (text.includes('취미') || text.includes('그림') || text.includes('창작'))) score += 3;
  if (category === 'monetize' && (text.includes('수익') || text.includes('부업') || text.includes('유튜브'))) score += 3;
  if (query.toLowerCase().includes('chatgpt') && text.includes('chatgpt')) score += 2;
  return score;
}

async function fetchYoutubeSearch(query) {
  const url = `https://www.youtube.com/results?search_query=${encodeURIComponent(query)}`;
  const res = await fetch(url, {
    headers: {
      'user-agent': 'Mozilla/5.0',
      'accept-language': 'ko-KR,ko;q=0.9,en;q=0.8'
    }
  });
  if (!res.ok) throw new Error(`youtube ${res.status}`);
  return res.text();
}

function decodeText(value = '') {
  return value
    .replace(/\\u0026/g, '&')
    .replace(/\\u003c/g, '<')
    .replace(/\\u003e/g, '>')
    .replace(/\\"/g, '"');
}

function extractVideosFromYoutubeHtml(html) {
  const regex = /"videoId":"([^"]+)"[\s\S]*?"title":\{"runs":\[\{"text":"([^"]+)"\}\][\s\S]*?"longBylineText":\{"runs":\[\{"text":"([^"]+)"/g;
  const seen = new Set();
  const items = [];
  let match;

  while ((match = regex.exec(html)) && items.length < MAX_PER_QUERY) {
    const [, id, rawTitle, rawChannel] = match;
    if (!id || seen.has(id)) continue;
    seen.add(id);
    items.push({
      id,
      url: `https://www.youtube.com/watch?v=${id}`,
      embedUrl: `https://www.youtube.com/embed/${id}`,
      title: decodeText(rawTitle),
      thumbnail: `https://i.ytimg.com/vi/${id}/hqdefault.jpg`,
      channel: decodeText(rawChannel)
    });
  }

  return items;
}

function dedupeById(items) {
  const map = new Map();
  for (const item of items) {
    if (!map.has(item.id)) map.set(item.id, item);
  }
  return [...map.values()];
}

async function discoverCategory(category, customQuery = '') {
  const queries = customQuery
    ? [customQuery]
    : (CATEGORY_QUERY_SETS[category] || [CATEGORY_QUERIES.intro]);

  const groups = await Promise.all(queries.map(async (query) => {
    const html = await fetchYoutubeSearch(query);
    const videos = extractVideosFromYoutubeHtml(html);
    return videos.map((item) => ({
      ...item,
      category,
      categoryLabel: CATEGORY_LABELS[category] || category,
      categoryGroup: CATEGORY_GROUPS[category] || 'Custom',
      query,
      score: scoreVideo(item, query, category),
      discoveredAt: new Date().toISOString()
    }));
  }));

  return dedupeById(groups.flat())
    .sort((a, b) => b.score - a.score)
    .slice(0, MAX_PER_CATEGORY);
}

async function discoverAllCategories() {
  const keys = Object.keys(CATEGORY_QUERIES);
  const results = await Promise.all(keys.map(async (key) => ({
    key,
    label: CATEGORY_LABELS[key],
    group: CATEGORY_GROUPS[key],
    items: await discoverCategory(key)
  })));
  return results;
}

